module Data.S57.Attributes
    ( Attribute(..)
    , AttributeT(..)
    ) where


data AttributeType
    = AttEnumerated
    | AttList
    | AttFloat
    | AttInteger
    | AttCodedString
    | AttFreeText
    deriving (Eq, Show)

data AttributeClass = AttributeClass Char

class (Enum a, Show a) => Attribute a where
    att_code :: a -> Int
    att_code = fromEnum
    att_desc :: a -> String
    att_acronym :: a -> String
    att_acronym = show
    att_type' :: a -> Char
    att_type :: a -> AttributeType
    att_type a =
        case (att_type' a) of
          'E' -> AttEnumerated
          'L' -> AttList
          'F' -> AttFloat
          'I' -> AttInteger
          'A' -> AttCodedString
          'S' -> AttFreeText
    att_class' :: a -> Char
    att_class :: a -> AttributeClass
    att_class a = AttributeClass $ att_class' a

